#include<stdio.h>

 int main()
{
    printf("Hello world! \n");          //for printing simple hello world

    printf("Hello\nworld! \n");        //for printing with /n scape sequence

    printf("\"Hello world!\" \n");    //for printing inside quotes("")

    printf("Hello\tworld! \n");      //for printing with a whit space

    printf("\'Hello world!\' \n");  //for printing with single quotes('')

    printf("\\Hello world!\\ \n");  //for printing with a slash (\)

return 0;

}